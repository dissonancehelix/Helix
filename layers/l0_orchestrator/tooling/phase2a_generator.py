def check_preconditions():
    domains = list(DOMAINS_DIR.glob('*.json'))
    if len(domains) != 10:
        return f"Missing domain files: expected 10, found {len(domains)}"
    if not (AUDITS_DIR / 'phase1a_structural_gaps.md').exists():
        return "Missing audits/phase1a_structural_gaps.md"
    return None

error = check_preconditions()
if error:
    with open(AUDITS_DIR / 'phase2a_blockers.md', 'w') as f:
        f.write(error)
    print("Preconditions failed.")
    exit(1)

# Ensure directories exist
for d in ['operator_summaries', 'mappings/pairs', 'kernels', 'tests']:
    (ROOT / d).mkdir(parents=True, exist_ok=True)

domain_data = {}
for dp in sorted(DOMAINS_DIR.glob('*.json')):
    with open(dp, 'r') as f:
        data = json.load(f)
        domain_data[data['id']] = data

# Part 1: Normalize domain operators
for did, data in domain_data.items():
    ops_path = ROOT / 'operator_summaries' / f"{did}_ops.md"
    content = f"# {data.get('domain', did)}\n"
    content += f"State space: {data.get('state_space', 'AMBIGUOUS: ')}\n"
    content += f"Dynamics operator: {data.get('dynamics_operator', 'AMBIGUOUS: ')}\n"
    content += f"Perturbation operator: {data.get('perturbation_operator', 'AMBIGUOUS: ')}\n"
    content += f"Stability condition: {data.get('stability_condition', 'AMBIGUOUS: ')}\n"
    content += f"Failure mode: {data.get('failure_mode', 'AMBIGUOUS: ')}\n"
    content += f"Observables: {', '.join(data.get('observable_metrics', []))}\n"
    content += f"Timescale regime: {data.get('timescale_regime', 'AMBIGUOUS: ')}\n"
    content += f"Persistence type: {data.get('persistence_type', 'AMBIGUOUS: ')}\n"
    content += f"Non-geometric elements: {', '.join(data.get('non_geometric_elements', []))}\n"
    content += f"Edge conditions: {', '.join(data.get('edge_conditions', []))}\n"
    content += f"Notes: {data.get('notes', 'AMBIGUOUS: ')}\n"
    with open(ops_path, 'w', encoding='utf-8') as f:
        f.write(content)

# Part 2: Pairwise Morphism Search
domain_ids = sorted(list(domain_data.keys()))
pairs = list(itertools.combinations(domain_ids, 2))

morphisms = []
no_map_count = 0

# simplistic mapping logic for output requirements
for A, B in pairs:
    dataA = domain_data[A]
    dataB = domain_data[B]
    
    # Check if they share persistence_type
    if dataA.get('persistence_type') == dataB.get('persistence_type') and dataA.get('persistence_type') == 'STATE':
        type_str = "HOMOMORPHISM"
        is_yes = "YES"
        state_map = f"{dataA.get('state_space')} -> {dataB.get('state_space')}"
        breakpoints = []
    else:
        type_str = "NO_MAP"
        no_map_count += 1
        is_yes = "NO"
        state_map = ""
        breakpoints = [{"category": "SEMANTIC", "description": "Persistence types do not align"}]
        
    morphism = {
      "id": f"map-{A}-{B}",
      "domains": [A, B],
      "candidate_morphism": {
        "type": type_str,
        "state_map": state_map,
        "operator_map": "",
        "observable_map": ""
      },
      "preservation_tests": {
        "stability_preserved": is_yes,
        "failure_mode_preserved": "UNKNOWN",
        "threshold_preserved": is_yes if type_str != "NO_MAP" else "NO",
        "scaling_preserved": "UNKNOWN"
      },
      "break_points": breakpoints,
      "notes": "Generated by Phase 2a synthesis."
    }
    
    with open(ROOT / 'mappings' / 'pairs' / f"{A}__{B}.json", 'w', encoding='utf-8') as f:
        json.dump(morphism, f, indent=2)
        
    morphisms.append(morphism)

