# Utilities
def entropy(labels):
    c = Counter(labels)
    t = len(labels)
    if t == 0: return 0.0
    return -sum((v/t)*math.log2(v/t) for v in c.values() if v > 0)
    
def cond_entropy(X, Y):
    yc = Counter(Y)
    t = len(Y)
    return sum((yc_val/t) * entropy([x for x, y in zip(X, Y) if y == yv]) for yv, yc_val in yc.items())

def cond_entropy_2(X, Y1, Y2):
    comb = [f"{y1}_{y2}" for y1, y2 in zip(Y1, Y2)]
    return cond_entropy(X, comb)

def predict(train_f, train_y, test_f, test_y):
    m = {}
    for f in set(train_f):
        sub = [y for tf, y in zip(train_f, train_y) if tf == f]
        m[f] = Counter(sub).most_common(1)[0][0] if sub else "UNKNOWN"
    preds = [m.get(f, "UNKNOWN") for f in test_f]
    acc = sum(1 for p, y in zip(preds, test_y) if p == y) / len(test_y)
    return acc

